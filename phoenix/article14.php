				<p class="con">中安在线讯 (通讯员：吴晓泉) 日前，来自省、市工艺美术协会及部门的领导，凤阳县的凤画工作者和爱好者汇聚一堂，以展示传统凤阳凤画精品和凤画新作及创作成果等方式，纪念凤画老艺人华荣生先生诞辰九十五周年。</p><br /><br />
				
				<p class="con">有着600多年历史，被称为“皖北三绝”之一的凤阳凤画，于2007年又成功申报为安徽省首批非物资文化遗产项目。 </p><br />

				<p class="con">凤阳籍凤画家华荣生老先生是华氏凤阳凤画的重要传承人，凤阳凤画是华荣生老先生上下几代人口传面授，传承下来的、有着独特的地方特色和艺术风格的民间艺术。近些年来，在各级党委政府和文化部门的关心重视下，通过凤画老艺人、凤画工作者和爱好者的共同努力，这朵艺术奇葩，绽放的更加灿烂，许多作品在国内外的美术殿堂上展出、获奖；凤画新作《大包干凤还巢》和《中华祥云图》分别被国家博物馆和国家奥组委收藏；吴文军、吴德椿、涂维良、王金生、张维武等被安徽省选定为工艺美术大师和民间工艺美术名人；境内外的新闻媒体多次频频报道出镜；由老、中、青、少不同年龄结构的凤画工作者和爱好者的创作队伍已经形成；在继承传统凤画特点的基础上，“情感凤画”、“主题凤画”、“花鼓凤画”、“水晶凤画”等不同风格和表现形式的创新凤画相继问世；“凤画系列旅游工艺产品”正在研制开发，目前，该县宣传、文化旅游、“非遗”保护中心等部门，又在为2009年申报“中国凤画之乡”称号作筹备。</p><br />
				
