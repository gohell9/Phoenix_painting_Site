				<p class="con">10月10日，凤阳县举办凤阳凤画研讨会，隆重纪念凤阳凤画杰出代表人华荣生先生诞辰100周年，县美术家协会和县非遗保护中心近30人参加了研讨会。</p><br /><br />
				<img src="images/a12-2.jpg" class="con-pic"/>
				<p class="con">研讨会上，凤画传承人张维武详细介绍了华荣生的生平及凤画创作经历，与会人员畅谈了华荣生对凤阳凤画的贡献，交流了华荣生有关凤阳凤画的历史事件，老、中、青三代凤画人畅所欲言，共同缅怀这位凤画大师的历史功绩，对华荣生在凤画传承上的无私奉献精神予以充分的肯定。 </p><br />

				<p class="con">凤阳凤画“代表性”传承人、省首届“工艺美术大师”吴文军在会上展示了他与华荣生交往的书信原件，并饱含深情朗读了华荣生的亲笔回信。县老年大学凤画班束加艺说：“华派凤画颜色鲜明，特点突出，是凤阳凤画独树一帜的标杆。华老对凤阳凤画的贡献是不可磨灭的，值得我们永远怀念。” </p><br />
				<p class="con">据了解，华荣生，中国凤阳凤画院名誉院长、特级画师，1915年10月15日生于凤阳县，2008年8月4日在石家庄逝世。他绘画的《百鸟朝凤》、《龙凤呈祥》、《飞向世界》、《凤兮归来》等画作多次在《人民日报》、《安徽日报》上刊登。上世纪80年代，他编写了《凤画简介》、《凤画教材》等书，发表了多篇有关凤画的专题论文，并数次在凤阳举办“凤画培训班”，培养青年凤画人才，被誉为凤阳凤画杰出代表、“华氏凤画嫡系传承人”。 </p><br />
